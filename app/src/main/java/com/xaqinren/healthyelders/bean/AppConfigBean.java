package com.xaqinren.healthyelders.bean;

public class AppConfigBean {

    private String mainAppletsId;
    private String baseUrl;
    private String telephone;
    private String aboutUsPage;
    private String userServicePage;
    private String userPrivacyPage;
    private String userReportPage;

    public String getMainAppletsId() {
        return mainAppletsId;
    }

    public void setMainAppletsId(String mainAppletsId) {
        this.mainAppletsId = mainAppletsId;
    }

    public String getBaseUrl() {
        return baseUrl;
    }

    public void setBaseUrl(String baseUrl) {
        this.baseUrl = baseUrl;
    }

    public String getTelephone() {
        return telephone;
    }

    public void setTelephone(String telephone) {
        this.telephone = telephone;
    }

    public String getAboutUsPage() {
        return aboutUsPage;
    }

    public void setAboutUsPage(String aboutUsPage) {
        this.aboutUsPage = aboutUsPage;
    }

    public String getUserServicePage() {
        return userServicePage;
    }

    public void setUserServicePage(String userServicePage) {
        this.userServicePage = userServicePage;
    }

    public String getUserPrivacyPage() {
        return userPrivacyPage;
    }

    public void setUserPrivacyPage(String userPrivacyPage) {
        this.userPrivacyPage = userPrivacyPage;
    }

    public String getUserReportPage() {
        return userReportPage;
    }

    public void setUserReportPage(String userReportPage) {
        this.userReportPage = userReportPage;
    }
}
